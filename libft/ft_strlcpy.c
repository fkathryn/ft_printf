#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t i;
	size_t l;

	i = 0;
	if (!dest || !src)
		return (0);
	l = ft_strlen(src);
	if (size == 0)
		return (l);
	while (src[i] != '\0' && i < (size - 1))
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (l);
}
